# Хеш-функция — функция, однозначно осуществляющая преобразование объекта произвольной длины
# в битовое значение (число) фиксированной длины.

# Коллизия — случай, когда у двух ключей хеши совпали.
# Ключ — так называют исходные данные.
# Идеальная хеш-функция — хеш-функция,
# которая без коллизий отображает различные ключи из множества объектов на множество хешей
# за O(1) времени в худшем случае.